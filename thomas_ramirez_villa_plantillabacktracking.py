"""## 9. Funciones auxiliares para grillas

Estas funciones buscan posiciones, imprimen grillas y calculan vecinos.

"""

def imprimir_grilla(grilla):
    for fila in grilla:
        print(" ".join(fila))

def copiar_grilla(grilla):
    return [fila[:] for fila in grilla]

def buscar_posicion(grilla, simbolo):
    for i in range(len(grilla)):
        for j in range(len(grilla[0])):
            if grilla[i][j] == simbolo:
                return (i, j)
    return None

def distancia_manhattan(a, b):
    return abs(a[0] - b[0]) + abs(a[1] - b[1])

def obtener_candidatos_rover(posicion):
    fila, columna = posicion

    movimientos = [
        (-1, 0),  # arriba
        (1, 0),   # abajo
        (0, -1),  # izquierda
        (0, 1)    # derecha
    ]

    candidatos = []
    for df, dc in movimientos:
        candidatos.append((fila + df, columna + dc))

    return candidatos

def esta_dentro(grilla, posicion):
    fila, columna = posicion
    return 0 <= fila < len(grilla) and 0 <= columna < len(grilla[0])

def es_valido_rover(grilla, posicion, visitados):
    if not esta_dentro(grilla, posicion):
        return False

    fila, columna = posicion

    if grilla[fila][columna] == "#":
        return False

    if posicion in visitados:
        return False

    return True

print("Grilla con solución:")
imprimir_grilla(grilla_con_solucion)
print("Inicio:", buscar_posicion(grilla_con_solucion, "S"))
print("Meta:", buscar_posicion(grilla_con_solucion, "G"))

"""## 10. Backtracking para ROVER-5

La función intentará encontrar un camino desde `S` hasta `G`.

También usará contadores:

- llamadas recursivas;
- ramas descartadas;
- retrocesos.

La poda opcional usa energía: si la distancia Manhattan a la meta es mayor que la energía restante, no se sigue por esa rama.

"""

def resolver_rover(grilla, usar_poda=True, energia_maxima=None):
    inicio = buscar_posicion(grilla, "S")
    meta = buscar_posicion(grilla, "G")

    if inicio is None or meta is None:
        raise ValueError("La grilla debe tener un inicio S y una meta G.")

    camino = [inicio]
    visitados = {inicio}

    estadisticas = {
        "llamadas_recursivas": 0,
        "ramas_descartadas": 0,
        "retrocesos": 0
    }

    def backtracking(posicion_actual, energia_restante):
        estadisticas["llamadas_recursivas"] += 1

        # Caso base: llegamos a la meta.
        if posicion_actual == meta:
            return True

        for candidato in obtener_candidatos_rover(posicion_actual):

            if not es_valido_rover(grilla, candidato, visitados):
                estadisticas["ramas_descartadas"] += 1
                continue

            # Poda opcional por energía y distancia mínima.
            if usar_poda and energia_restante is not None:
                energia_despues = energia_restante - 1

                if energia_despues < 0:
                    estadisticas["ramas_descartadas"] += 1
                    continue

                if distancia_manhattan(candidato, meta) > energia_despues:
                    estadisticas["ramas_descartadas"] += 1
                    continue
            else:
                energia_despues = None if energia_restante is None else energia_restante - 1

            # Tomar decisión.
            camino.append(candidato)
            visitados.add(candidato)

            if backtracking(candidato, energia_despues):
                return True

            # Retroceso.
            camino.pop()
            visitados.remove(candidato)
            estadisticas["retrocesos"] += 1

        return False

    encontrado = backtracking(inicio, energia_maxima)
    return encontrado, camino, estadisticas

"""## 11. Visualización de la solución

Marcamos el camino encontrado con `*`.

"""

def grilla_con_camino(grilla, camino):
    nueva = copiar_grilla(grilla)

    for fila, columna in camino:
        if nueva[fila][columna] not in ("S", "G"):
            nueva[fila][columna] = "*"

    return nueva


