mayusculas = ["A", "B", "C"]
minusculas = ["a", "b", "c"]
digitos = ["1", "2", "3"]
simbolos = ["!", "#"]

caracteres_permitidos = mayusculas + minusculas + digitos + simbolos

politica = {
    "longitud": 5,
    "requiere_mayuscula": True,
    "requiere_minuscula": True,
    "requiere_digito": True,
    "requiere_simbolo": True,
    "no_repetir": True,
    "palabras_prohibidas": ["abc", "123", "pass"]
}

print("Caracteres permitidos:", caracteres_permitidos)
print("Política:", politica)

"""## 3. Funciones auxiliares

Primero escribimos funciones pequeñas para verificar categorías y restricciones.

"""

def contiene_mayuscula(cadena):
    return any(c in mayusculas for c in cadena)

def contiene_minuscula(cadena):
    return any(c in minusculas for c in cadena)

def contiene_digito(cadena):
    return any(c in digitos for c in cadena)

def contiene_simbolo(cadena):
    return any(c in simbolos for c in cadena)

def contiene_palabra_prohibida(cadena, palabras_prohibidas):
    texto = "".join(cadena).lower()
    return any(palabra.lower() in texto for palabra in palabras_prohibidas)

def categorias_faltantes(cadena, politica):
    faltantes = 0

    if politica["requiere_mayuscula"] and not contiene_mayuscula(cadena):
        faltantes += 1

    if politica["requiere_minuscula"] and not contiene_minuscula(cadena):
        faltantes += 1

    if politica["requiere_digito"] and not contiene_digito(cadena):
        faltantes += 1

    if politica["requiere_simbolo"] and not contiene_simbolo(cadena):
        faltantes += 1

    return faltantes

def es_contrasena_completa_valida(cadena, politica):
    if len(cadena) != politica["longitud"]:
        return False

    if politica["requiere_mayuscula"] and not contiene_mayuscula(cadena):
        return False

    if politica["requiere_minuscula"] and not contiene_minuscula(cadena):
        return False

    if politica["requiere_digito"] and not contiene_digito(cadena):
        return False

    if politica["requiere_simbolo"] and not contiene_simbolo(cadena):
        return False

    if contiene_palabra_prohibida(cadena, politica["palabras_prohibidas"]):
        return False

    return True

def es_candidato_valido(cadena_actual, candidato, politica):
    if politica["no_repetir"] and candidato in cadena_actual:
        return False

    nueva = cadena_actual + [candidato]

    if contiene_palabra_prohibida(nueva, politica["palabras_prohibidas"]):
        return False

    if len(nueva) > politica["longitud"]:
        return False

    return True

def debe_podar(cadena_actual, politica):
    posiciones_restantes = politica["longitud"] - len(cadena_actual)
    faltantes = categorias_faltantes(cadena_actual, politica)

    # Si faltan más categorías obligatorias que posiciones disponibles,
    # esta rama ya no puede producir una contraseña válida.
    return faltantes > posiciones_restantes

print("Categorías faltantes en ['A', 'b']:", categorias_faltantes(["A", "b"], politica))

"""## 4. Backtracking para generar contraseñas

La función construye la contraseña carácter por carácter.

Contadores:

- `llamadas_recursivas`: cuántas veces entramos a la función.
- `ramas_descartadas`: candidatos rechazados por restricciones o poda.
- `retrocesos`: cuántas veces deshacemos una decisión.

"""

def generar_contrasenas(politica, limite=10):
    soluciones = []

    estadisticas = {
        "llamadas_recursivas": 0,
        "ramas_descartadas": 0,
        "retrocesos": 0
    }

    def backtracking(cadena_actual):
        estadisticas["llamadas_recursivas"] += 1

        # Si ya tenemos suficientes soluciones, detenemos la búsqueda.
        if len(soluciones) >= limite:
            return

        # Poda sobre la solución parcial.
        if debe_podar(cadena_actual, politica):
            estadisticas["ramas_descartadas"] += 1
            return

        # Caso base: la cadena ya tiene longitud completa.
        if len(cadena_actual) == politica["longitud"]:
            if es_contrasena_completa_valida(cadena_actual, politica):
                soluciones.append("".join(cadena_actual))
            else:
                estadisticas["ramas_descartadas"] += 1
            return

        # Probar candidatos.
        for candidato in caracteres_permitidos:
            if es_candidato_valido(cadena_actual, candidato, politica):
                cadena_actual.append(candidato)

                backtracking(cadena_actual)

                # Retroceso: deshacer la decisión.
                cadena_actual.pop()
                estadisticas["retrocesos"] += 1
            else:
                estadisticas["ramas_descartadas"] += 1

    backtracking([])
    return soluciones, estadisticas


soluciones, stats = generar_contrasenas(politica, limite=10)

print("Contraseñas generadas:")
for s in soluciones:
    print("-", s)

print("\nEstadísticas:")
print(stats)

